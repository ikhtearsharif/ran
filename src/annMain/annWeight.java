
package annMain;

/*
 * This class will create weight
 * */
public class annWeight
{
	
	
	float a,b,c;
	
	public annWeight(float p,float q,float r)
	{
		this.a=p;
		this.b=q;
		this.c=r;
		
	}
	
	public float getA()
	{
		return this.a;
	}
	
	public float getB()
	{
		return this.b;
	}
	
	public float getC()
	{
		return this.c;
	}
	
	public void setA( float p)
	{
		this.a=p;
	}
	
	public void setB( float q)
	{
		this.b=q;
	}
	
	public void setC( float r)
	{
		this.c=r;
	}
}
